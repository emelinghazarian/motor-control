  HAL_TIM_Encoder_Start(&htim2, TIM_CHANNEL_ALL);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);

  HAL_ADC_Init(&hadc1);

  LCD_Init();
  HAL_Delay(400);
  LCD_Clear();

  char line[16];
  int goal = 0;

  long cc = 0, pc = 0, dc = 0; // current count, previous count, delta
  long plt = 0, clt = 0, dt; // previous loop time, current loop time, delta time

  float crpm = 0; // current rpm

  float d, d2, cs; // delta, previous delta, control signal

  /* USER CODE END 2 */

